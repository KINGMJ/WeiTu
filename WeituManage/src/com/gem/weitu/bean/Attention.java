package com.gem.weitu.bean;

public class Attention {
	private String attention_id;
	private String user_id;
	private String att_id;
	private int att_group;
	public String getAttention_id() {
		return attention_id;
	}
	public void setAttention_id(String attention_id) {
		this.attention_id = attention_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getAtt_id() {
		return att_id;
	}
	public void setAtt_id(String att_id) {
		this.att_id = att_id;
	}
	public int getAtt_group() {
		return att_group;
	}
	public void setAtt_group(int att_group) {
		this.att_group = att_group;
	}
	
	
	
	
	

}
